# Access Array Items

pokemon_evolution = ['Charmander', 'Charmeleon', 'Charizard']

first_item = pokemon_evolution[0]
# Charmander

second_item = pokemon_evolution[1]
# Charmeleon

last_item = pokemon_evolution[-1]
# Charizard

penultimate_item = pokemon_evolution[-2]
# Charmeleon

print(first_item, second_item, last_item, penultimate_item)

# Access Tuple Items

another_pokemon_evolution = ('Gastly', 'Haunter', 'Gengar',) 
print(another_pokemon_evolution[0])
# Gastly

print(another_pokemon_evolution[-1])
# Gengar