# set remove repeated values
numbers = set([1, 2, 3, 4, 5, 1, 2, 3])
print(numbers)
# {1, 2, 3, 4, 5}

print(set(numbers))
# {1, 2, 3, 4, 5}

numbers = {1, 2, 3, 4, 5, 1, 2, 3}
print(numbers)
# {1, 2, 3, 4, 5}

letters = set('principe')
print(letters)
# {'e', 'p', 'n', 'i', 'r', 'c'}

worlds = set(('computer', 'computer', 'food', 'health', 'body'))
print(worlds)
# {'food', 'computer', 'health', 'body'}