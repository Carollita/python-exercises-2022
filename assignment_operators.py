cost = 10
# we use the assignment operator (=) to assign the value 10 to a variable called cost

cost += 5
# The addition assignment operator (+=) adds a value to a variable, same as (cost = cost + 5)

cost -= 5 
# same as (cost = cost - 5)

cost *= 5 
# same as (cost = cost * 5)

cost //= 5 
# same as (cost = cost // 5)

cost /= 5 
# same as (cost = cost / 5)


cost %= 5 
# same as (cost = cost % 5)

cost **= 5 
# same as (cost = cost ** 5)

print(cost)